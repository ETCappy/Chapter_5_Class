//loops are a  repetition of a process that runs a fixed number of times
//three parts: initialization, start;conditional, end;increment
public class Loop_1 {

	  public static void main(String[] args) {
		  
		 //start at 1, count to 5, increment by 1
		  
		  for(int i = 1; i <=5 ; i++) {
			  System.out.println(i + "");


		      }
		    }
	  
}