//1. do while initialize, 2. conditional, 3. increment, 
//do only means to continue on
//do while is checked at the end
//the loop will always run 1x


public class DoWhile_1 {

	public static void main(String[] args) {
	
		int i = 1;

	do 
	
	{
		
		System.out.println(); //notice the semi colon at the end
		// ; signals the end
		i++;
	
	
	} while (i <= 5);
	
	System.out.println();

}

}
