//increment by 5
public class While_2 {

	public static void main(String[] args) {
		int i = 5; //always initialize outside of the loop- 1
		while(i <= 25) {  //2 says where the loop ends
			
			System.out.println( i + "");
			
			i+=5;  //3 counts up
			
		}


	
	}
	
}
